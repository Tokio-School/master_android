package com.example.clase38_calculadoratest

class Calculadora {
    public fun sumar(a: Int, b: Int): Int {
        return a + b
    }
    public fun restar(a: Int, b: Int): Int {
        return a - b
    }
    public fun multiplicar(a: Int, b: Int): Int {
        return a * b
    }
    public fun dividir(a: Int, b: Int): Int {
        return a / b
    }
}